class Solution {
public:

    // Encodes a URL to a shortened URL.
    string s;
    string s1;
    string encode(string longUrl) {
        s=longUrl;
        return s1;
    }

    // Decodes a shortened URL to its original URL.
    string decode(string shortUrl) {
        s1=shortUrl;
        return s;
    }
};

